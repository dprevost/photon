/**
 *  Applications can send two types of messages. One when they first 
 *  connect to request all needed parameters (file name of VDs, for example)
 *  and one when they disconnect.
 */
enum  WDOpcode
{
   WD_CONNECT = 1,
   WD_DISCONNECT
};

/**
 *  This struct is used by the applications to send information to the 
 *  watchdog.
 */

struct WDInput
{
   enum  WDOpcode opcode;
   pid_t processId;
};


/**
 * This struct is used by the watchdog to send information back to the
 *  applications.
 */

struct WDOutput
{
   char pathname[PATH_MAX];
   size_t memorySizekb;
};

