/**
 *  Applications can send two types of messages. One when they first 
 *  connect to request all needed parameters (file name of shared memory, 
 *  for example) and one when they disconnect.
 */
enum  qsrOpcode
{
   QSR_CONNECT = 1,
   QSR_DISCONNECT
};

/**
 *  This struct is used by the applications to send information to quasar
 */
struct qsrInput
{
   enum  qsrOpcode opcode;
   pid_t processId;
};


/**
 * This struct is used by quasar to send information back to the
 * applications.
 */

struct qsrOutput
{
   char pathname[PATH_MAX];
   size_t memorySizekb;
};

