#include "API/Process.h"

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

int main()
{
   vdsaProcess process;
   int errcode;

   memset( &process, 0, sizeof(vdsaProcess) );
   /* try something junk... */   
   errcode = vdsaProcessInit( &process, "abcdef" );

   if ( errcode != VDS_INVALID_WATCHDOG_ADDRESS ) {
      fprintf( stderr, "err: %d\n", errcode );
      return -1;
   }
   
   return 0;
}

