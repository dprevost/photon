/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

#include "procMgrTest.h"

psonProcMgr* pManager;
psonSessionContext context;
psonProcess * process;

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void setup_test()
{
   bool ok;
   
   pManager = initProcMgrTest( &context );

   ok = psonProcMgrInit( pManager, &context );
   assert( ok );
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void teardown_test()
{
   if (g_pBaseAddr) free(g_pBaseAddr);
   g_pBaseAddr = NULL;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_null_context( void ** state )
{
#if defined(PSO_UNIT_TESTS)
   expect_assert_failure( psonProcMgrAddProcess( pManager, 
                                                 12345, 
                                                 &process,
                                                 NULL ) );
#endif
   return;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_null_manager( void ** state )
{
#if defined(PSO_UNIT_TESTS)
   expect_assert_failure( psonProcMgrAddProcess( NULL, 
                                                 12345, 
                                                 &process,
                                                 &context ) );
#endif
   return;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_null_process( void ** state )
{
#if defined(PSO_UNIT_TESTS)
   expect_assert_failure( psonProcMgrAddProcess( pManager, 
                                                 12345, 
                                                 NULL,
                                                 &context ) );
#endif
   return;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_zero_pid( void ** state )
{
#if defined(PSO_UNIT_TESTS)
   expect_assert_failure( psonProcMgrAddProcess( pManager, 
                                                 0, 
                                                 &process,
                                                 &context ) );
#endif
   return;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_pass( void ** state )
{
#if defined(PSO_UNIT_TESTS)
   bool ok;
   
   ok = psonProcMgrAddProcess( pManager, 
                               12345, 
                               &process,
                               &context );

   assert_true( ok );
   
#endif
   return;
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

int main()
{
   int rc = 0;
#if defined(PSO_UNIT_TESTS)
   const UnitTest tests[] = {
      unit_test_setup_teardown( test_null_context, setup_test, teardown_test ),
      unit_test_setup_teardown( test_null_manager, setup_test, teardown_test ),
      unit_test_setup_teardown( test_null_process, setup_test, teardown_test ),
      unit_test_setup_teardown( test_zero_pid,     setup_test, teardown_test ),
      unit_test_setup_teardown( test_pass,         setup_test, teardown_test )
   };

   rc = run_tests(tests);
   
#endif
   return rc;
}

